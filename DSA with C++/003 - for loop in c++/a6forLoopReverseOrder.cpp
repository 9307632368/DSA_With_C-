// print numbers in reverse order
#include<iostream>
using namespace std;
int main(){
    for(int i=10;i>=1;i--){
        cout<<i<<endl;
    }

    int number=9;
     cout<<"Enter a number: ";
     cin>>number;
    for(int j=number;j>=1;j--){
        cout<<j<<endl;
    }

    return 0;
}