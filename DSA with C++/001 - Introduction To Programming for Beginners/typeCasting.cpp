#include<iostream>
using namespace std;
int main(){
    // type casting
    int a=10;
    char x='A';

    a=x;
    cout<<a<<endl;

    int b=80;
    char y='c';

    y=b;
    cout<<y<<endl;

    //typecasting int to long int
    int shortNumber=10;
    long int largeNumber;

    largeNumber=shortNumber;
    cout<<largeNumber<<endl;

   //typecasting long int to int
    long long int longNumber= 9307632368;
    cout<<longNumber<<endl;
    int smallNumber;
    smallNumber=longNumber;
    cout<<smallNumber<<endl;

   
}